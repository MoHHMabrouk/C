#ifndef _MATHLIB_H_
#define _MATHLIB_H_

/**
 * @brief Calculate the factorial of an integer.
 *
 * @param number The integer for which to calculate the factorial.
 * @return long long The factorial of the input integer.
 */
long long MATH_voidIntFact(long long number);

/**
 * @brief Calculate the nth term in the Fibonacci sequence.
 *
 * @param number The position of the term in the Fibonacci sequence.
 * @return long long The value of the nth term in the Fibonacci sequence.
 */
long long MATH_voidIntFabbo(long long number);

#endif // _MATHLIB_H_
